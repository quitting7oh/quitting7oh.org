// scripts/sync-site-changelog.mjs
//
// Regenerates src/content/about/changelog.md from the repo-root CHANGELOG.md
// so the public-facing changelog page on the site stays in sync with the
// canonical git changelog. Idempotent: re-running with no upstream changes
// only updates the last_updated date.
//
// Run manually with `npm run sync:changelog`, or rely on the `prebuild`
// hook to run it automatically before `npm run build`.

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, '..');
const SRC = path.join(repoRoot, 'CHANGELOG.md');
const DST = path.join(repoRoot, 'src/content/about/changelog.md');

const today = new Date().toISOString().slice(0, 10);

const raw = await fs.readFile(SRC, 'utf8');

// Strip the repo CHANGELOG's H1 + intro paragraph; the site page renders
// its own title from frontmatter and adds its own reader-facing intro.
// We keep everything from the first "## " section header onward.
const firstSection = raw.search(/\n## /);
const body = firstSection >= 0 ? raw.slice(firstSection + 1).trim() : raw.trim();

const header = `---
title: "Changelog"
description: "Recent changes to quitting7oh.org: new pages, rewrites, design tweaks, and editorial sweeps. Newest entries first."
category: "about"
last_updated: "${today}"
sort: 6
---

<!--
  Auto-generated from /CHANGELOG.md by scripts/sync-site-changelog.mjs.
  Do not edit this file directly. Edit /CHANGELOG.md and re-run the sync
  (or just commit; the prebuild hook runs the sync automatically).
-->

This page tracks substantive changes to the site: new pages, rewrites, layout work, sourcing passes, editorial cleanups. Newest entries at the top. The same content lives in [\`CHANGELOG.md\`](https://github.com/quitting7oh/quitting7oh.org/blob/main/CHANGELOG.md) at the repo root, which is the source this page is generated from.

`;

const out = header + body + '\n';

// Read the current destination (if any) so we can short-circuit when nothing
// substantive changed. last_updated will always differ; compare the body.
let prevBody = '';
try {
  const prev = await fs.readFile(DST, 'utf8');
  // Strip the frontmatter block from the previous file before comparison.
  const fmEnd = prev.indexOf('\n---\n', 4);
  prevBody = fmEnd >= 0 ? prev.slice(fmEnd + 5) : prev;
} catch {
  // File doesn't exist yet; we'll create it.
}

const newBody = out.slice(out.indexOf('\n---\n', 4) + 5);

if (prevBody.trim() === newBody.trim()) {
  // Body identical; don't touch the file (preserves the existing
  // last_updated date so we don't churn dates on every build).
  console.log('[sync-site-changelog] no changes; site changelog already in sync.');
  process.exit(0);
}

await fs.writeFile(DST, out);
console.log(`[sync-site-changelog] wrote ${path.relative(repoRoot, DST)} (last_updated=${today})`);
